using System;

namespace _10._Multiplication_Table
{
    class Program
    {
        static void Main(string[] args)
        {
            int n = int.Parse(Console.ReadLine());

            for (int i = 1; i <= 1; i++)
            {
                for (int j = 1; j <= 10; j++)
                {
                    Console.WriteLine($"{n} X {j} = {n*j}");
                }
            }
        }
    }
}
